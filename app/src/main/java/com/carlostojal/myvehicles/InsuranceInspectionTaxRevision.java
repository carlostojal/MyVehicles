public class InsuranceInspectionTaxRevision {
    Date date;
    float value;

    public InsuranceInspectionTaxRevision(Date date, float value) {
        this.date = date;
        this.date = date;
        this.value = value;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public float getValue() {
        return value;
    }

    public void setValue(float value) {
        this.value = value;
    }
}
